// ─── SourceKind ─────────────────────────────────────────────────────────────

/// Which external system the import file/stream originates from.
#[derive(Debug, Clone, Copy, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
#[serde(rename_all = "SCREAMING_SNAKE_CASE")]
pub enum SourceKind {
    SmartbillXml,
    SmartbillRest,
    SagaXml,
    SagaDbf,
    WinmentorTxt,
}

impl std::fmt::Display for SourceKind {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        let s = match self {
            SourceKind::SmartbillXml => "SMARTBILL_XML",
            SourceKind::SmartbillRest => "SMARTBILL_REST",
            SourceKind::SagaXml => "SAGA_XML",
            SourceKind::SagaDbf => "SAGA_DBF",
            SourceKind::WinmentorTxt => "WINMENTOR_TXT",
        };
        write!(f, "{s}")
    }
}

// ─── ImportInput ────────────────────────────────────────────────────────────

/// The raw data handed to an adapter for parsing.
pub enum ImportInput {
    /// Raw file bytes (TXT / XML / DBF / ZIP).
    Bytes(Vec<u8>),
    /// One or more file paths (e.g. Partener.txt + Articole.txt + invoice.txt).
    Files(Vec<std::path::PathBuf>),
    /// REST-API credentials (used by SmartBillRest adapter in W3).
    RestCreds { company_id: String },
}

