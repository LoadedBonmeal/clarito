// ─── canonical_cui helper ────────────────────────────────────────────────────

/// Normalise a Romanian CUI/CIF for use as a dedup key.
///
/// Chain (matches contacts.rs normalization + strips leading zeros for staging):
///   1. trim whitespace
///   2. uppercase
///   3. strip leading "RO" (VAT-payer prefix is not part of identity)
///   4. trim again (handles "RO 123" with interior space)
///   5. strip leading ASCII zeros (canonical numeric identity; "0123" == "123")
///
/// The result is purely numeric for valid RO CUIs. Foreign VAT ids
/// (non-numeric after strip) are returned as-is (uppercased, trimmed).
pub fn canonical_cui(raw: &str) -> String {
    let s = raw.trim().to_uppercase();
    let s = s.strip_prefix("RO").unwrap_or(&s);
    let s = s.trim();
    // Strip leading zeros so "0123", "000", and "123" all compare equal.
    let stripped = s.trim_start_matches('0');
    if stripped.is_empty() {
        // Input was all zeros (or empty after stripping RO).
        // Return "0" when there was at least one zero digit (dedup: "000" == "0").
        // Return "" when there was truly nothing.
        if s.is_empty() {
            String::new()
        } else {
            "0".to_string()
        }
    } else {
        stripped.to_string()
    }
}

// ─── Tests ──────────────────────────────────────────────────────────────────

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn canonical_cui_strips_ro_prefix_and_leading_zeros() {
        // "RO 0123" and "123" must produce the same key.
        assert_eq!(canonical_cui("RO 0123"), canonical_cui("123"));
        // Lowercase prefix also stripped.
        assert_eq!(canonical_cui("ro0123"), canonical_cui("123"));
        // No prefix, no zeros.
        assert_eq!(canonical_cui("12345678"), "12345678");
        // Already canonical.
        assert_eq!(canonical_cui("1234567"), "1234567");
        // Interior spaces in prefix area handled.
        assert_eq!(canonical_cui("RO  007"), "7");
        // All zeros → "0" not empty.
        assert_eq!(canonical_cui("000"), "0");
        // Bare zero.
        assert_eq!(canonical_cui("0"), "0");
        // Trimming whitespace.
        assert_eq!(canonical_cui("  RO 256644  "), "256644");
    }

    #[test]
    fn canonical_cui_roundtrip_ro_prefix_variants() {
        // The brief explicitly lists "RO12345678" (VAT-payer form) and "12345678"
        // (non-VAT-payer form) as the SAME legal entity.
        let with_prefix = canonical_cui("RO12345678");
        let without_prefix = canonical_cui("12345678");
        assert_eq!(with_prefix, without_prefix);
    }
}
